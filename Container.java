public class Container {
	//Private variables
	private int number;
	private int specialNumber;
	private String specialProperty;
	
	//Constructor
	public Container(int number, int specialNumber, String specialProperty) {
		this.number = number;
		this.specialNumber = specialNumber;
		this.specialProperty = specialProperty;
	}

	//Getter and Setter
	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public int getSpecialNumber() {
		return specialNumber;
	}

	public void setSpecialNumber(int specialNumber) {
		this.specialNumber = specialNumber;
	}

	public String getSpecialProperty() {
		return specialProperty;
	}

	public void setSpecialProperty(String specialProperty) {
		this.specialProperty = specialProperty;
	}
	
	public void updateThis(){
		this.number = 1;
	}
	
	
}
	
